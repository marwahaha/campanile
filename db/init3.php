<?php
   class MyDB extends SQLite3
   {
      function __construct()
      {
         $this->open('test.db');
      }
   }
   $db = new MyDB();
   if(!$db){
      echo $db->lastErrorMsg();
   } else {
      echo "Opened database successfully\n";
   }

   $sql =<<<EOF
      INSERT INTO BRICKS (ID,NAME,MESSAGE)
      VALUES (1, 'Kunal', 'first message');

      INSERT INTO BRICKS (ID,NAME,MESSAGE)
      VALUES (2, 'Arun', 'poem two' );
EOF;

   $ret = $db->exec($sql);
   if(!$ret){
      echo $db->lastErrorMsg();
   } else {
      echo "Records created successfully\n";
   }
   $db->close();
?>